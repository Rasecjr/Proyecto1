	this.getUbigeo = function (callback) {
		if (position) {
			geocoder.geocode({ 'location': position }, function (results, status) {
				var addresses = results[0].address_components;
				var location = results[0].geometry.location;
                var formattedAddress = results[0].formatted_address;
                
				setPais(addresses);
				setDepartamento(addresses);
				setProvincia(addresses);
				setLatLng(location);
                setDistrito(results);
                ubigeoResult.direccion = formattedAddress;
                callback(ubigeoResult);
			});
		} else {
			geocoder.geocode(opts, function (results, status) {
				if (status === 'OK' && results.length > 0) {
					var addresses = results[0].address_components;
					var location = results[0].geometry.location;

					ubigeoResult.direccion = address;
					setPais(addresses);
					setDepartamento(addresses);
					setProvincia(addresses);
					setLatLng(location);

                    geocoder.geocode({ 'location': location }, function (results, status) {
                        setDistrito(results);
                        callback(ubigeoResult);
					});
				}
			});
        }
	};

